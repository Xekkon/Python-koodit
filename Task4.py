num1 = int(input("Enter the first integer number: "))
num2 = int(input("Enter the second integer number: "))
num3 = int(input("Enter the third integer number: "))

sum = num1 + num2 + num3
product = num1 * num2 * num3
average = sum / 3

print(f"The sum of the three numbers are: {sum}" )
print(f"The product of the three numbers are: {product}")
print(f"The average of the three numbers are: {average}")

